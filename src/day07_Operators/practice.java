package day07_Operators;

public class practice {
    public static void main(String[] args) {

        int z = 45;
        System.out.println(++z);//46
        System.out.println(z++);//46
        System.out.println(z);//47
        int q = 30;
        System.out.println(++q);
        System.out.println(q);//29
        System.out.println(q);//28

    }
}
