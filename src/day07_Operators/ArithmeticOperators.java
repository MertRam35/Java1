package day07_Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int num1= 10;
        int num2=3;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        double num3= num2;
        int num4= num1/num2;
        System.out.println(num1/num3);
        System.out.println(num1*num2);
        System.out.println(num1%num2);
        int num5 = num1 %num2;
        System.out.println(num1 + " divided by "+num2 + " is equal to "+num4+" with remainder is "+num5 );
    }
}
