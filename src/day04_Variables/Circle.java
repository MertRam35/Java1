package day04_Variables;

public class Circle {
    public static void main(String[] args) {

        double r = 10;

        int PI = 25;
        double area = PI * r * r;
        System.out.println("area = " + area);
        double perimeter = 2 * PI * r;
        System.out.println("perimeter = " + perimeter);

    }

}

