package day03_EscapeSequences;

public class Face {

    public static void main(String[] args) {

        System.out.println(" +''''''''''+");
        System.out.println("[|   O O   |]");
        System.out.println(" |    ^    |");//ZCDVXV
        System.out.println(" |   '_'   |");
        System.out.println(" +_________+");

        System.out.println("                    ^\n");
        System.out.println("                   / \\\n");
        System.out.println("                  /   \\\n");
        System.out.println("                 /     \\");

        /*

                    ^
                   / \
                  /   \
                 /     \
                /       \
                ---------
         */
    }
}
